        <h1>CON POLICIES</h1>
    </div>
    <div id="con-policies">
    <p class="subtitle">Click on the item to view details.</p>
        <details>
            <summary>1. Break: Follow That Reference Exactly</summary>
            <p>When making a cosplay, cosplayers often use one or more pictures as a reference to make sure that they get the cosplay how they want. While this is common practice, many people add their own personal touch to the outfit. This can be a great form of self-expression and is often praised despite not exactly matching the original reference. Also, most people do not look exactly like the character they are portraying – and that’s perfectly okay!</p>
        </details>
        <details>
            <summary>2. Break: Don’t Bother Others</summary>
            <p>Bothering another cosplayer for any reason is a no go. Most cosplayers know where the line is, but other cosplayers have reported being bothered for various things; dates, personal information, photographs, and more things that they do not want to do. To people who do this, please do not. Cosplay is not consent and no means no. Bothering someone makes them less likely to do what you want. Treat others with respect.</p>
        </details>
        <details>
            <summary>3. Follow: Remain In Character When On-Duty</summary>
            <p>When you are in costume as a cosplayer, you become the character you are wearing. With that in mind, a general rule is to act exactly like your character when you are not taking a break. While in costume, many people are watching you, which means you need to be extra mindful of your actions. This should be fun since you chose that particular character and cosplay for a reason, but this is a general unsaid rule that is good to follow.</p>
        </details>
        <details>
            <summary>4. Break: Take It Seriously</summary>
            <p>For some, cosplay is serious business. However, most people get into cosplay because it is fun. Cosplay is a chance to express yourself and your love for a particular show, franchise, or character. Cosplay is a fun hobby or career, so it is good to treat it that way. Be creative and try new things, you may surprise yourself and make something even more awesome. Cosplay is super fun to do, so having a lighthearted approach to it can help you keep at it.</p>
        </details>
        <details>
            <summary>5. Follow: Do Not Insult Other Cosplayers </summary>
            <p>Following on a similar vein from previous rules, this one is basic. Many cosplayers are proud of their work and can be nervous to take it out in public. It takes bravery to pull that off and hurtful comments are completely unnecessary. Some newer cosplayers may even quit altogether because of some peoples’ tactless comments. The cosplay community can be a supportive and wholesome place – let’s keep it that way as best we can so everyone has fun.</p>
        </details>
        <details>
            <summary>6. Follow: Ask Permission Before Joining A Photoshoot </summary>
            <p>Doing a photo shoot is one of the coolest things a cosplayer can do. It is arguably the ultimate way to showcase your work and modeling abilities. Many cosplayers’ photoshoot photos have gone viral and gotten them great opportunities, so you can understand why folks choose to do them. Often, they have a professional photographer taking the photos and they have a set plan in mind. If you see a cosplay photoshoot going on, ask permission to join in and take your own pictures so you don’t accidentally get in the way.</p>
        </details>
        <details>
            <summary>7. Break: Don’t Mock Beginners </summary>
            <p>Okay, not all cosplayers do this. However, it is a big issue in the community. Some folks out there have taken it upon themselves to tell people that they cannot cosplay a certain character because they don’t ‘look the part’. This cosplayer may have a different look from the original reference and comments like this are not encouraging to cosplayers at all. This gatekeeping needs to stop and is unacceptable. This type of behavior is disrespectful and hurtful and could drive people away from the community – not cool at all.</p>
        </details>
        <details>
            <summary>8. Follow: Don’t Bother Cosplayers When They Are Eating</summary>
            <p>Being in character can be an exhilarating experience, but it can also be exhausting to maintain all day. Taking regular breaks to drink water, eat, and rest is essential to your wellbeing. Most folks follow the unspoken rule that when a cosplayer is on break, you do not bother them for photos or signatures. Nobody wants to be bothered while they are eating with friends and ‘off-duty’. If you really want that photo, wait until they are done eating and are back in character.</p>
        </details>
        <details>
            <summary>9. Follow: Cosplay Is Not Consent </summary>
            <p>A follow-up from what I discussed earlier, but it has to be emphasized; cosplay is not consent. I don’t care if the cosplayer is wearing full armor or a tiny outfit – you do NOT touch them without their consent or pressure them in any way. Just because they are dressed up as your fictional crush does not mean you can grab them or ask them for any kinds of favors. The person under the cosplay will have a big problem with your inappropriate behavior.</p>
        </details>
        <details>
            <summary>10. Break: Tag Others And Credit People’s Work</summary>
            <p>This one is fairly self-explanatory, but an important one to remember. When bringing to life an artist or photographer’s unique take on a particular character it is common courtesy to credit them in your descriptions and photos. If you take pictures with other cosplayers, it is good practice to get their social media handles and tag them in photos you share after the convention. Crediting and tagging folks means that the community grows and others get to see some fabulous work.</p>
        </details>
        <details>
            <summary>11. Follow: Drink Lots Of Water</summary>
            <p>Being in a crowded indoor place all day can mean that it gets hot in cons really fast. Everyone’s body heat and sometimes the weather all come together to make con-goers a little hot under the collar. To make sure you stay healthy and feel good, bring plenty of water in your bag when you go to conventions. It can be difficult to remember to drink when you are having so much fun doing all the activities and making friends. Taking care of yourself is just as important as having fun.</p>
        </details>
        <details>
            <summary>12. Follow: Adhere To Con Weapon Rules</summary>
            <p>As I briefly mentioned earlier, different cons will have different rules and regulations regarding cosplay and props that must be followed. Often, there are rules regarding weapons. Sometimes it may be that you have to unstring a bow or take the tips off of arrows, but sometimes there are regulations regarding size and some don’t let you carry weapon props at all. Make sure to double check these rules before you head out to the convention just in case.</p>
        </details>
        <details>
            <summary>13. Break: Don’t Hog The Spotlight </summary>
            <p>Being proud of your hard work is absolutely to be encouraged. Making cosplay takes a lot of effort and skill, no matter how experienced the cosplayer is. However, some folks take it a bit too far. Hogging the spotlight, especially at panels or when you are in a group cosplay, is a big no-no. Being proud of your work is great, but let others show off their hard work, too. No-one likes to feel pushed out or ignored, so just bear it in mind.</p>
        </details>
        <details>
            <summary>14. Follow: Don’t Hog People’s Time</summary>
            <p>When a cosplayer hosts a panel or photo op for their fans, it can be a super fun (and profitable) experience. If the cosplayer is particularly popular, long lines form and time limits are put in place so as many people as possible can join in. However, some folks take it too far and try to impose on the cosplayer or hog all their time. It can make the cosplayer feel awkward and uncomfortable or stop others from having their turn.</p>
        </details>
        <details>
            <summary>15. Follow: Be Careful In Big Costumes</summary>
            <p>As any mech anime fan will tell you, there are some huge costumes out there. Characters in shows and movies often end up wearing massive suits or huge armor. These are hard to replicate and mad props to anyone who can make these kinds of cosplay, however, they can be dangerous. Huge cosplay can block the way or step on someone. If it has large protrusions or spikes then you could poke someone’s eyes out by accident. Just announce that you’re walking by with an ‘excuse me’ and it should be fine.</p>
        </details>
        <details>
            <summary>16. Break: Pay For Tickets</summary>
            <p>Certain high-profile cosplayers out there seem to think that they can just show up to a con or event dressed in a perfect cosplay and just walk on in without buying a ticket. Just because you are in a fancy cosplay does not mean that you get special treatment. Conventions are pedestrian events that everyone has to buy tickets for. Buying tickets supports the convention and everyone who works there so it can keep growing. It is rude and disrespectful to so blatantly break the rules.</p>
        </details>
        <details>
            <summary>17. Follow: Do Not Bother Others</summary>
            <p>Over the years, there have been many stories and allegations of bothering and invasion of privacy at conventions. It ranges from cosplayers getting grabbed or touched to outright disrespected at conventions against their consent and sometimes even against their knowledge. There are too many to discuss here, but some more high-profile cases include Momo-Kun’s serial issues and the secret film in a cosplay changing room. Both of these are examples of what not to do, so take notes.</p>
        </details>
        <details>
            <summary>18. Follow: Respect Others’ Props</summary>
            <p>Many cosplayers make their own props to go with their cosplay. The prop may be a staff, toy, weapon, or other accessories that make the character pop. These props are usually handmade, delicate, or both. Knowing this, do not just grab a cosplayer’s prop without respect or permission. You don’t know how much work and time went into it and the possibility that their prop could be broken is stressful. Once again, ask for permission first and it should be fine.</p>
        </details>
        <details>
            <summary>19. Break: Don’t Block The Way</summary>
            <p>Okay, so this often is not the cosplayer’s fault. Sometimes, they get stopped by someone wanting a photo and soon enough more and more people come until it’s a crowd. The problem is this is usually in a hallway or walkway so no-one can get past. This is often out of the cosplayer’s control but is still annoying for others to deal with. However, sometimes someone in a large bulky costume stands still in a hallway for no reason and it is the worst.</p>
        </details>
        <details>
            <summary>20. Follow: Don’t Act Dumb In Cosplay</summary>
            <p>When choosing someone to cosplay in public, one of the biggest sources for character inspiration is children’s characters. When you are in cosplay, especially as a kids’ character, you must be mindful of how you act. Do not say or do anything that your character won’t do while you are in costume. Cursing when they would not do so is not okay, especially if there are young children around. When in costume, even on break, people will be watching, so be careful.</p>
        </details>
        <details>
            <summary>21. Follow: Don’t Touch Without Consent</summary>
            <p>No-one wants someone to just grab them when they aren’t expecting it, especially if they shove a camera in your face. Many cosplayers have experienced unwanted photos, comments, and physical contact at conventions and events. If you want to hug or touch a cosplayer, always ask permission; if you don’t and you grab them, that is a crime. Respect their agency and ask for permission and you both will have a way better time.</p>
        </details>
        <details>
            <summary>22. Break: Cosplay Is For One Body Type</summary>
            <p>NOT true. Cosplay is for anyone no matter what you look like or what skill you have. People who body shame are the ones who are wrong and toxic. Who cares as long as you are expressing yourself and having a good time? No-one is going to pull off an exact copy of your fave character, but they will come up with a cute version that lets them have fun. Cosplay is supposed to be fun; there is no place for gatekeeping in this community.</p>
        </details>
        <details>
            <summary>23. Follow: Ask For Permission Before Filming Or Taking Photographs</summary>
            <p>While cosplayers are happy that folks admire their cosplays and are flattered that you want to take pictures, there is an unsaid rule; ask for permission before filming them. You would think that most folks would know this, but this is actually a big issue at cons. There are many other rules for photography but this is the most basic one. Just ask for permission first and everyone will have a good time and you’ll get a cute photo with your fave.</p>
        </details>
        <details>
            <summary>24. Follow: Wear Deodorant</summary>
            <p>Cons and events are often inside events that are packed with people. Folks are often in heavy cosplay and there are large crowds and it just turns into a hot mess. With that in mind, cosplayers (and just people in general) need to wear deodorant. It’s hard work being in character and costume all day and you can work up a sweat doing it. Just do everyone a favor and wear some deodorant so there’s no stank up in the con.</p>
        </details>
        <details>
            <summary>25. Break: Don’t Film In Forbidden Areas</summary>
            <p>Getting the perfect selfie in cosplay is often a goal for many social media savvy cosplayers. One of the best places to do that is well-lit bathrooms. When a convention is happening, you can always find cosplayers posting photo shoots and cute selfies all over social media. However, just check where you are and make sure you are mindful of who or what is around you while taking pictures in case you catch something private or unexpected.</p>
        </details>
