    <div id="faqs-content">
        <p class="subtitle">Click on the item to view the answer.</p>
        <details>
            <summary>1. Can I cosplay _____________ ?</summary>
            <p> Yes. Next question. Ok, maybe I should explain more.
                So I hear a lot of people ask if they can cosplay a certain character
                from a game/anime/tv show/movie or whatever. But they are concerned that
                they aren’t the right body type, gender, skin color or whatever. Cosplay
                is about bringing your love to the character NOT about being a doppelganger.
                In recent years, I’ve seen cosplay competitions start to put emphasis on the
                actual construction of the costume and not just how much someone physically
                looks like the character. Cosplay who you want and don’t listen to the haters,
                they’re just jealous that they didn’t make an awesome costume. Quick note though,
                don’t change your skin color to match the character unless they are green or purple
                or something unnatural. Just trust me on this, I’ve seen plenty of people make this
                mistake and it totally overshadows their otherwise great costume.</p>
        </details>
        <details>
            <summary>2. Can I buy my costume?</summary>
            <p>Yes, but just know you can’t enter costume contests. If you just want to go to
                a convention and dress up as your favorite character but don’t have the time
                to make the costume yourself, there is nothing wrong with buying your costume
                . You can buy one from many of the cosplay stores online, but beware the quality
                of some won’t be that great. Alternatively, you could support your fellow makers
                and commission someone to make it specifically for you. That option is generally
                more expensive but you can get a much better result. Just be sure to do your
                research and pick a reputable maker with a lot of good and recent reviews.

                To repeat: you CANNOT enter a competition if you didn’t make your costume. Don’t
                try it, you will be found out. Now you can have a PIECE of your costume that was
                bought or commissioned. Each convention usually will put in their rules what the
                ratio is, like 80% must be made by you, so just make sure you follow the rules.
                For example, I’m terrible at wigs, so if the wig is above my skill level, I usually
                commission that piece.</p>
        </details>
        <details>
            <summary>3. Can I do an original design or “inspired by” costume?</summary>
            <p>Honestly, those are some of my favorite cosplays that I have seen. Like “Master Chef”
                or “Disco Darth Vader”. Some competitions don’t allow original designs, so just read
                the rules if you want to compete. Also I myself have done an "inspired by" costume
                with Okami and it was a lot of fun to flex my creative muscles.</p>
        </details>
        <details>
            <summary>4. What do I do if someone makes me uncomfortable at a convention?</summary>
            <p>If there is a con creeper, or someone is making you uncomfortable for any reason, first
                tell them to stop. I know it can be scary but it generally makes the action stop
                immediately. If you are with a group of friends, watch out for each other and get to
                safety. Now this next part is up to you, but you can report that person to con security.
                Con staff is usually floating around the convention and  you can report it to them or they
                will direct  you to a booth to report the behavior. It may lead to that person being
                ejected from the convention and you are making the convention a safer place. </p>
        </details>
        <details>
            <summary>5. Are there any convention etiquette tips for con goers and cosplayers?</summary>
            <p>Here are some basic ones for all con goers: shower everyday, make time to eat, hydrate
                throughout the day, ask before you take photos of cosplayers, and go in with a budget
                (and stick to it).
                For cosplayers specifically: same as all the above plus, it’s ok to say “no” to a photo,
                schedule some break time in, don’t block “traffic” for photos especially if you have a big
                costume.
                Alright, so those are just some of the frequently asked questions that I hear about.
                If there are any that I missed or you have suggestions for additional topics for me
                to cover, please leave a comment down below. That’s all for this time and I’ll see
                ya’ll later!</p>
        </details>
